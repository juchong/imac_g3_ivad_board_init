/**
 * @file imacG3IvadInit.h
 * @brief EEPROM configuration and EDID data
 *
 * Contains:
 * - EEPROM version constant for compatibility checking
 * - EEPROM config offset enumeration (25 settings + checksum)
 * - Serial communication constants (buffer size, EOL marker)
 * - Active 128-byte EDID data for iMac G3 DV
 *
 * EEPROM Layout (26 bytes total):
 * - Bytes 0-24: Configuration settings (21) + reserved (4)
 * - Byte 25:    Checksum for validation
 *
 * EDID Source:
 * Extracted from iMac G3 DV by oshimai (MacRumors forum)
 * Supports 1024x768@75Hz, 800x600@95Hz, 640x480@117Hz
 *
 * @see ivad.h for IVAD register definitions
 */
const byte CONFIG_EEPROM_VERSION = 0x03;

/**
 * @brief EEPROM configuration offset enumeration
 *
 * Defines byte offsets for each setting stored in EEPROM.
 * These map to IVAD_SETTING_XXX enum values (same indices).
 *
 * Total: 25 configuration bytes + 1 checksum byte = 26 bytes
 *
 * Offset Map:
 * | Offset  | Setting        | Size |
 * |---------|----------------|------|
 * | 0-20    | 21 IVAD settings | 21B |
 * | 21-24   | Reserved/unused | 4B  |
 * | 25      | Checksum        | 1B  |
 *
 * @note Last enum value CONFIG_EEPROM_SLOTS = 26 (total byte count)
 */
enum EEPROM_CONFIG_OFFSET : const byte
{
  CONFIG_OFFSET_CONTRAST,
  CONFIG_OFFSET_RED_DRIVE,
  CONFIG_OFFSET_GREEN_DRIVE,
  CONFIG_OFFSET_BLUE_DRIVE,
  CONFIG_OFFSET_RED_CUTOFF,
  CONFIG_OFFSET_GREEN_CUTOFF,
  CONFIG_OFFSET_BLUE_CUTOFF,
  CONFIG_OFFSET_HORIZONTAL_POS,
  CONFIG_OFFSET_HEIGHT,
  CONFIG_OFFSET_VERTICAL_POS,
  CONFIG_OFFSET_S_CORRECTION,
  CONFIG_OFFSET_KEYSTONE,
  CONFIG_OFFSET_PINCUSHION,
  CONFIG_OFFSET_WIDTH,
  CONFIG_OFFSET_PINCUSHION_BALANCE,
  CONFIG_OFFSET_PARALLELOGRAM,
  CONFIG_OFFSET_RESERVED6,
  CONFIG_OFFSET_BRIGHTNESS,
  CONFIG_OFFSET_ROTATION,
  CONFIG_OFFSET_CHECKSUM,
  CONFIG_EEPROM_SLOTS  // Total slots = 26

};

